import json
import time
import os
from llama_index.core import VectorStoreIndex, KeywordTableIndex, Document, Settings
from llama_index.embeddings.openai import OpenAIEmbedding
from llama_index.llms.openai import OpenAI

# 1. Configure global LlamaIndex settings with your credentials
Settings.embed_model = OpenAIEmbedding(
    model='text-embedding-3-small',
    api_key=OPENAI_APIKEY
)
Settings.llm = OpenAI(
    model='gpt-4o-mini',
    api_key=OPENAI_APIKEY
)

# 2. Load the synthetic data
with open('data/synthetic_tickets.json', 'r', encoding='utf-8') as f:
    tickets = json.load(f)

# Convert raw data into LlamaIndex Document nodes
documents = []
for t in tickets:
    doc = Document(
        text=f"Title: {t['title']}\nDescription: {t['description']}\nResolution: {t['resolution']}",
        metadata={'ticket_id': t['ticket_id'], 'category': t['category']}
    )
    documents.append(doc)

print("Building indexes for documents...\n")

# --- BENCHMARK 1: Vector Store Index (Semantic Search) ---
start = time.time()
vector_index = VectorStoreIndex.from_documents(documents)
vector_time = time.time() - start
print(f"-> Vector Index build time: {vector_time:.2f} seconds")

# --- BENCHMARK 2: Keyword Table Index (Exact Matching) ---
start = time.time()
keyword_index = KeywordTableIndex.from_documents(documents)
keyword_time = time.time() - start
print(f"-> Keyword Index build time: {keyword_time:.2f} seconds\n")

# --- COMPARING QUERIES SIDE-BY-SIDE ---
# Test 1: Conceptual Query
query_semantic = "authentication login problem"
print("=" * 60)
print(f"QUERY: '{query_semantic}'")
print("=" * 60)

vec_engine = vector_index.as_query_engine(similarity_top_k=1)
vec_response = vec_engine.query(query_semantic)
print(f"Vector Index Result:\n  {str(vec_response).strip()}\n")

# Test 2: Exact Ticket ID Query
query_exact = "TICK-005"
print("=" * 60)
print(f"QUERY: '{query_exact}'")
print("=" * 60)

kw_engine = keyword_index.as_query_engine()
kw_response = kw_engine.query(query_exact)
print(f"Keyword Index Result:\n  {str(kw_response).strip()}")



