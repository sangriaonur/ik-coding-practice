# --- HOUR 2: EXERCISE 6 & 7 (VECTOR STORE & FILTERING) ---
import json
from langchain_community.vectorstores import Chroma
from langchain_openai import OpenAIEmbeddings
from langchain_core.documents import Document

# 1. Initialize OpenAI Embeddings with your key
embeddings_model = OpenAIEmbeddings(
    model='text-embedding-3-small',
    openai_api_key=OPENAI_APIKEY
)

# 2. Load and create documents with metadata attached
with open('data/synthetic_tickets.json', 'r') as f:
    tickets = json.load(f)

documents = []
for ticket in tickets:
    doc = Document(
        page_content=f"Title: {ticket['title']}. Description: {ticket['description']}",
        metadata={
            'ticket_id': ticket['ticket_id'],
            'category': ticket['category'],
            'priority': ticket['priority']
        }
    )
    documents.append(doc)

# 3. Create the Chroma Vector Store directly in Colab's RAM
print("Embedding documents and building Chroma Vector Database...")
chroma_store = Chroma.from_documents(
    documents,
    embeddings_model,
    collection_name="colab_exercise_store"
)
print("✓ Chroma DB is populated and ready!\n")

# --- EXERCISE 6 & 7 TASK: Search with combined metadata filters ---
query = "system not working"

# We use an $and logical filter to look ONLY for 'Authentication' tickets that are 'High' priority
combined_filter = {
    "$and": [
        {"category": "Authentication"},
        {"priority": "High"}
    ]
}

print(f"Running query: '{query}'")
print("Applying structural filter: Category == 'Authentication' AND Priority == 'High'\n")

# Run search returning BOTH the matching document and its calculated Distance score
results_with_scores = chroma_store.similarity_search_with_score(query, k=3, filter=combined_filter)

print(f"Results Found ({len(results_with_scores)}):")
print("-" * 60)
for i, (doc, score) in enumerate(results_with_scores, 1):
    print(f"#{i} - L2 Distance Score: {score:.4f}")
    print(f"    Ticket ID: {doc.metadata['ticket_id']}")
    print(f"    Priority:  [{doc.metadata['priority']}]")
    print(f"    Category:  [{doc.metadata['category']}]")
    print(f"    Content:   {doc.page_content[:100]}...")
    print()



